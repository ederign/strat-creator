def _env(jira):
    return {
        **os.environ,
        "JIRA_SERVER": jira.url,
        "JIRA_USER": "admin",
        "JIRA_TOKEN": "admin",
    }


def _run(jira, strat_key, local_dir, extra_args=None):
    cmd = [sys.executable, SCRIPT, strat_key, "--local-dir", str(local_dir)]
    if extra_args:
        cmd.extend(extra_args)
    return subprocess.run(
        cmd,
        env=_env(jira),
        capture_output=True,
        text=True,
        cwd=PROJECT_ROOT,
    )


def _setup_strat_with_rfe(jira, strat_key, rfe_key, labels=None):
    """Create a STRAT issue linked to an RFE via Cloners."""
    desc_md = (
        "## Business Need (from RFE)\n\n"
        "Users need GPU sharing.\n\n"
        "## Strategy (AI Generated by Agentic SDLC Pipeline)\n\n"
        "### Technical Approach\n\n"
        "Use MIG profiles for GPU time-slicing.\n"
    )
    jira.create(strat_key, "GPU time-slicing strategy", desc_md,
                labels=labels or [])
    jira.create(rfe_key, "GPU sharing RFE",
                "Users need cost-efficient GPU sharing.")

    jira.request("POST", "/rest/api/3/issueLink", {
        "type": {"name": "Cloners"},
        "inwardIssue": {"key": strat_key},
        "outwardIssue": {"key": rfe_key},
    })


class TestPullWithRubricPass:

    def test_pulls_strategy_file(self, jira, tmp_path):
        _setup_strat_with_rfe(jira, "RHAISTRAT-2000", "RHAIRFE-3000",
                              labels=["strat-creator-rubric-pass"])

        local_dir = tmp_path / "local"
        result = _run(jira, "RHAISTRAT-2000", local_dir)
        assert result.returncode == 0, f"stderr: {result.stderr}"

        strat_path = local_dir / "strat-tasks" / "RHAISTRAT-2000.md"
        assert strat_path.exists()

        data, body = read_frontmatter(str(strat_path))
        assert data["strat_id"] == "RHAISTRAT-2000"
        assert data["workflow"] == "local"
        assert data["source_rfe"] == "RHAIRFE-3000"
        assert "GPU time-slicing" in body

