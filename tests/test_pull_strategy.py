        _upload_strategy_attachment(jira, "RHAISTRAT-2040", full_strategy)

        local_dir = tmp_path / "local"
        result = _run(jira, "RHAISTRAT-2040", local_dir)
        assert result.returncode == 0, f"stderr: {result.stderr}"
        assert "attachment" in result.stdout.lower()

        strat_path = local_dir / "strat-tasks" / "RHAISTRAT-2040.md"
        content = strat_path.read_text()
        assert "Business Need" in content
        assert "Users need GPU sharing" in content
